#!/bin/bash

# ============================================
# 쿠팡 소싱 도우미 - 프로덕션 서버 실행
# ============================================

echo "=========================================="
echo "  쿠팡 소싱 도우미 - 프로덕션 서버"
echo "=========================================="
echo ""

# 환경변수 파일 확인
if [ ! -f ".env.local" ]; then
    echo "⚠️  .env.local 파일이 없습니다."
    exit 1
fi

# 빌드 확인
if [ ! -d ".next" ]; then
    echo "📦 빌드 시작..."
    npm run build
    if [ $? -ne 0 ]; then
        echo "❌ 빌드 실패"
        exit 1
    fi
fi

echo "🚀 프로덕션 서버 시작..."
echo "   접속 주소: http://localhost:3000"
echo ""

npm start
